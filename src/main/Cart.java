    /**
     * Remove an item from the shopping cart.
     * 
     * @param evt 
     */
    private void removeItemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeItemButtonActionPerformed
        int selectedItemIndex = cartList.getSelectedIndex();
        
        if (selectedItemIndex < 0) {
            JOptionPane.showMessageDialog(this, "No product selected");
            return;
        }
        
        selectedItemsName.remove(selectedItemIndex);
        cartList.setModel(selectedItemsName);
    }//GEN-LAST:event_removeItemButtonActionPerformed
    
    /**
     * Create and show a new checkout frame.
     * 
     * @param evt 
     */
    private void checkoutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkoutButtonActionPerformed
        if (selectedItemsName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Cart is empty.");
            return;
        }
        
        (new Checkout(this)).setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_checkoutButtonActionPerformed

